console.log(Math.round(4.5))//gettin nearest integer value 5
console.log(Math.round(4.8))
console.log(Math.round(3.2))

//ceil method
console.log(Math.ceil(4.9))//gettin above value 5
console.log(Math.ceil(4.1))//5
console.log(Math.ceil(4.0))//4

//floor method
console.log(Math.floor(4.3))//getting lower value
console.log(Math.floor(5.8))
console.log(Math.floor(-8.9))

//trunc method 
console.log(Math.trunc(4.2))
console.log(Math.trunc(7.8))
console.log(Math.trunc(-9.5))

//Math.sign()
console.log(Math.sign(-4))
console.log(Math.sign(-7))
console.log(Math.sign(8))
console.log(Math.sign(0))

